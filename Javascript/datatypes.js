// Data types - Primitive
// any types of number
let length = 20.1;
length = -10;
console.log(length);

// strings - can be enclosed in " " or '
let greeting = "Let's go!";
// let greeting = 'Let's go!' <--- will cause an error;
console.log(greeting);

// boolean = true or false
let isStudent = true;
console.log(isStudent);

// underlined - uninitialized or missing value
let city;
console.log(city)

// null - intentional absence of an object value
let car = null;
console.log(car);

